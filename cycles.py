# Получаем максимальное число
max_number = int(input())

# Инициализируем переменные
row = 1  # номер текущей строки
number = 1  # текущее число для вывода

# Пока не достигли максимального числа
while number <= max_number:
    col = 1  # сбрасываем счетчик столбцов для новой строки

    # Выводим числа для текущей строки
    while col <= row and number <= max_number:
        print(number, end=' ')
        col += 1
        number += 1

    # Переходим на новую строку
    print()
    row += 1  # переходим к следующей строке